'''Module for GinJinn configuration errors.
'''

class InvalidInputConfigurationError(Exception):
    '''Error representing invalid input configuration.
    '''

class InvalidModelConfigurationError(Exception):
    '''Error representing invalid model configuration.
    '''

class InvalidAugmentationConfigurationError(Exception):
    '''Error representing invalid augmentation configuration.
    '''

class InvalidGinjinnConfigurationError(Exception):
    '''Error representing invalid general GinJinn configuration.
    '''
